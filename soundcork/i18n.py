def normalize_language(language: object) -> str:
    value = str(language or "").strip().lower()
    return value if value in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def translation_bundle(language: str) -> dict[str, str]:
    return TRANSLATIONS[normalize_language(language)].copy()


def translate(key: str, language: str, **values: object) -> str:
    text = TRANSLATIONS[normalize_language(language)].get(
        key, ENGLISH_TRANSLATIONS.get(key, key)
    )
    if not values:
        return text
    try:
        return text.format(**values)
    except Exception:
        return text


def translate_message(message: str | None, language: str) -> str:
    if not message:
        return ""

    key = MESSAGE_KEY_MAP.get(message)
    if key:
        return translate(key, language)

    dynamic_patterns: tuple[tuple[str, str], ...] = (
        ("Saved backup ", "saved_backup"),
        ("Restored ", "restored_backup"),
    )
    for prefix, dynamic_key in dynamic_patterns:
        if message.startswith(prefix):
            filename = message[len(prefix) :]
            if dynamic_key == "saved_backup":
                return translate("saved_backup", language, filename=filename)
            return translate("restored_backup", language, filename=filename)

    if message.startswith("Saved ") and " to preset " in message:
        name, preset_number = message.removeprefix("Saved ").rsplit(" to preset ", 1)
        return translate(
            "saved_to_preset",
            language,
            name=name,
            preset_number=preset_number,
        )
    if message.startswith("Sleep in ") and message.endswith(" min"):
        minutes = message.removeprefix("Sleep in ").removesuffix(" min")
        return translate("sleep_in_minutes_notice", language, minutes=minutes)
    if message.startswith("Sleep at "):
        return translate("sleep_at", language, time=message.removeprefix("Sleep at "))
    if message.startswith("Alarm ") and " preset " in message:
        alarm_time, preset_id = message.removeprefix("Alarm ").split(" preset ", 1)
        return translate(
            "alarm_with_preset",
            language,
            time=alarm_time,
            preset_id=preset_id,
        )
    if message.startswith("Alarm test preset "):
        preset_id = message.removeprefix("Alarm test preset ")
        return translate("alarm_test_preset", language, preset_id=preset_id)

    return message
